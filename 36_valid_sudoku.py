def isValidSudoku(board):
    seen = set()
    for i in range(9):
        for j in range(9):
            symbol = board[i][j]
            if symbol != ".":
                cell_set = {f"{i}r{symbol}", f"{symbol}c{j}", f"{i//3}-{symbol}-{j//3}"}
                if cell_set & seen:
                    return False
                seen |= cell_set
    return True


print(isValidSudoku([["9", "3", ".", ".", "7", ".", ".", ".", "."],
                     ["6", ".", ".", "1", "9", "5", ".", ".", "."],
                     [".", "9", "8", ".", ".", ".", ".", "6", "."],
                     ["8", ".", ".", ".", "6", ".", ".", ".", "3"],
                     ["4", ".", ".", "8", ".", "3", ".", ".", "1"],
                     ["7", ".", ".", ".", "2", ".", ".", ".", "6"],
                     [".", "6", ".", ".", ".", ".", "2", "8", "."],
                     [".", ".", ".", "4", "1", "9", ".", ".", "5"],
                     [".", ".", ".", ".", "8", ".", ".", "7", "9"]]))  # True
